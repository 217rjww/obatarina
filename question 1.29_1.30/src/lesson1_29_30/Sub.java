package lesson1_29_30;

public class Sub {

	private String name;                              
	private String capital;
	private int area;

	public String getName() {                                //nameのgetterメソッド作成
		return name;
	}

	public void setName(String name) {                       //nameのsetterメソッド作成
		this.name = name;
	}

	public String getCapital() {                             //Capitalのgetterメソッド作成
		return capital;
	}

	public void setCapital(String capital) {                 //Capitalのsetterメソッド作成
		this.capital = capital;
	}

	public int getArea() {                                   //Areaのgetterメソッド作成
		return area;
	}

	public void setArea(int area) {                          //Areaのsetterメソッド作成
		this.area = area;
	}

}
